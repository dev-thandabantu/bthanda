// ─── FLAT LIST (for search + stats) ──────────────────────────────────────────

function flatten(person: FamilyPerson): FamilyPerson[] {
  const result: FamilyPerson[] = [person]
  if (person.children) {
    for (const child of person.children) {
      result.push(...flatten(child))
    }
  }
  return result
}

export const allPeople = flatten(familyTree)

